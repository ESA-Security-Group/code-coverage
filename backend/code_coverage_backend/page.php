<h2>Overview</h2>

<p><img src='https://scripts.sil.org/cms/scripts/../sites/nrsi/media/OFL_logo_rect_color.png' height='31' width='88'></p>
<p>The SIL Open Font License (OFL) is a free, libre and open source license specifically designed for fonts and related software based on our experience in font design and linguistic software engineering. </p>
<p>The OFL provides a legal framework and infrastructure for worldwide development, sharing and improvement of fonts and related software in a collaborative manner. It enables font authors to release their work under a common license that allows use, bundling, modification and redistribution. It encourages shared value, is not limited to any specific computing platform or environment, and can be used by other organizations or individuals.</p>
<p>The OFL meets the specific needs of typographic design and engineering as well as the gold standards of the FLOSS (Free/Libre and Open Source Software) community, namely the cultural values and guidelines from the FSF <span class='footnote_ref'><a href='#footnote_1' name='_ftnref_1'>1</a></span>, the Debian Free Software Guidelines<span class='footnote_ref'><a href='#footnote_2' name='_ftnref_2'>2</a></span>, as well as the Open Source Definition<span class='footnote_ref'><a href='#footnote_3' name='_ftnref_3'>3</a></span>. It draws inspiration from concepts and elements found in other licenses, but our improvements in the specific area of fonts have made the licensing model work better than other approaches currently in use.</p>
<p><a href='http://www.sil.org/' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;SIL International</a> serves language communities worldwide, building their capacity for sustainable language development, by means of research, translation, training and materials development. We have been thinking about more open and participative models for a while, for example through our partnerships with UNESCO (<a href='page.php?item_id=Babel'>Initiative B@bel</a>) and our work on the <a href='page.php?item_id=gentium'>Gentium</a> typeface. See <a href='http://www.sil.org/resources/software_fonts' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;www.sil.org/resources/software_fonts</a> for a detailed list of free/libre and open source software resources provided by SIL.</p>
<p>We want to:</p>
<ul class='dListUnordered'>
<li>enable others to participate in our projects </li>
<li>enable others to cater to needs for which we don't have the resources </li>
<li>share our wealth of knowledge and experience in the area of writing systems and pass on our tools</li>
<li>equip the community to meet its font needs </li>
</ul>
<p>We serve the peoples of the world without regard to their material wealth, so we are grateful to those that do fund our work. Please visit <a href='http://www.sil.org/donate' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;Donate to SIL International</a> for information on supporting our efforts.</p>
