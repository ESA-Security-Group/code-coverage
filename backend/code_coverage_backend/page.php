<h2>Using the OFL</h2>

<p>It is relatively simple to use the OFL for your own font project. If you are the copyright owner you only need to do the following:</p>
<ul class='dListUnordered'>
<li>Put your copyright and Reserved Font Names information at the beginning of the main OFL.txt file in place of the dedicated placeholders (marked with the &lt;&gt; characters). Include this file in your release package.</li>
<li>Put your copyright and the OFL text with your chosen Reserved Font Name(s) into your font files (the copyright and license fields). A link to the OFL text on the OFL web site is an acceptable (but not recommended) alternative. Also add this information to any other components (build scripts, glyph databases, documentation, test files, etc). Accurate metadata in your font files is beneficial to you as an increasing number of applications are exposing this information to the user. For example, clickable links can bring users back to your website and let them know about other work you have done or services you provide. Depending on the format of your fonts and sources, you can use template human-readable headers or machine-readable metadata. You should also double-check that there is no conflicting metadata in the font itself contradicting the license, such as the fstype bits in the os2 table or fields in the name table.</li>
<li>Write an initial FONTLOG.txt for your font and include it in the release package (see Section 6 and Appendix A of the OFL-FAQ for details including a template).</li>
<li>Include the relevant practical documentation on the license by adding the current OFL-FAQ.txt file in your package.</li>
<li>If you wish, you can use the <a href='page.php?item_id=OFL_logo'>OFL Graphics</a> on your web page.</li>
</ul>
<p>More information can be found in the <a href='page.php?item_id=OFL-FAQ_web'>OFL-FAQ</a>.</p>
