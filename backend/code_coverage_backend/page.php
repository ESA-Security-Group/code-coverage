<h3>Translations</h3>

<p>We also recognize the need for people who are not familiar with English to be able to understand the OFL and this FAQ better - in their own language. If you are an experienced translator, you are very welcome to help by translating the OFL and its FAQ so that designers and users in your language community can understand the license better. But only the original English version of the license has legal value and has been approved by the community. Translations do not count as legal substitutes and should only serve as a way to explain the original license. SIL - as the author and steward of the license for the community at large - does not approve any translation of the OFL as legally valid because even small translation ambiguities could be abused and create problems.</p>
<p>We give permission to publish unofficial translations into other languages provided that they comply with the following guidelines:</p>
<p>1) Put the following disclaimer in both English and the target language stating clearly that the translation is unofficial:</p>
<p><span class='Literal'> "This is an unofficial translation of the SIL Open Font License into $language. It was not published by SIL International, and does not legally state the distribution terms for fonts that use the OFL. A release under the OFL is only valid when using the original English text.</span></p>
<p><span class='Literal'> However, we recognize that this unofficial translation will help users and designers not familiar with English to understand the SIL OFL better and make it easier to use and release font families under this collaborative font design model. We encourage designers who consider releasing their creation under the OFL to read the FAQ in their own language if it is available.</span> <br />
<span class='Literal'> Please go to <a href='https://scripts.sil.org/OFL' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;https://scripts.sil.org/OFL</a> for the official version of the license and the accompanying FAQ."</span></p>
<p>2) Keep your unofficial translation current and update it at our request if needed, for example, if there is any ambiguity which could lead to confusion.  </p>
<p>If you start such a unofficial translation effort of the OFL and its accompanying FAQ please let us know, thank you.</p>
