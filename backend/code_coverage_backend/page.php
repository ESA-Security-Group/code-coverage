<h2>Documents</h2>

<p>We have gone through a lot of effort to make our license readable and easily understood by users, designers and software developers as well as package maintainers and distributors. To make the OFL even more human-readable, we have provided a FAQ (Frequently Asked Questions) to help everyone understand the intent and the practical aspects of using the license itself. Although it already covers many items, the FAQ will grow as needed. Please let us know if you have more questions.</p>
<a name='5667e9e4'></a>
<h3>Current version - 1.1</h3>

<p>We recommend all authors use version 1.1 of the OFL, but version 1.0 is given here for reference. A full list of changes from 1.0 to 1.1 can be found on the <a href='page.php?item_id=OFL_review'>OFL Review</a> page. The most important change for authors is that no font names are reserved by default. Reserved Font Names must be explicitly listed alongside the copyright statement in the OFL header.</p>
