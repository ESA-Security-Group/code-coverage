<a name='809bffa7'></a>
<h3>Community review</h3>

<p>Between November 2005 and January 2007 the OFL was in a public review stage, with efforts going towards version 1.1. We selected a number of reviewers we felt were the relevant experts and sought their input. We submitted our draft for review and received very insightful feedback.</p>
<p>The review period is over and even though we feel version 1.1 will likely meet the needs for open font licensing for quite some time, we remain open to community feedback. Please contact us with your queries and suggestions.</p>
<p>Various font-related BoFs (Birds of a Feather meetings) have taken place at FLOSS conference (like Libre Graphics Meeting, Ubuntu Summit, GUADEC, DebConf, TextLayoutSummit among others) to discuss what would be needed to improve the font landscape. One key aspect was appropriate licensing of the fonts, flexibility to maintain and branch fonts without breaking rendering, interoperability across distributions, and the definition of a core set of fonts with recognized glyph quality, sufficient Unicode coverage and a good community-recognized license. The OFL has been recognized by many contributors to these discussion as a good solution for these issues. </p>
<p>The goals of the OFL and its methodology have been presented and discussed at major conferences from the type industry like <a href='http://www.atypi.org' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;AtypI</a>.</p>
<p>Open font-related presentation have also been made at <a href='http://tug.org/' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;TUG (TeX User Group conferences)</a>.</p>
<p>There is a campaign with support from various key organizations in the FLOSS community (Unifont.org, Freedesktop.org, the GNOME foundation, KDE e.V., the Linux Foundation and the Free Software Foundation) to encourage more designers and supporting institutions to consider choosing the OFL for their font projects. Visit <a href='http://unifont.org/go_for_ofl/' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;Unifont.org/go_for_ofl</a> for more details and ways you can participate.</p>
<p>The OFL is now well-established as the most widely used licensing model for releasing and developing unrestricted font software. It is being used successfully by various organizations, both for-profit and not-for-profit, to release fonts of varying levels of scope and complexity. A number of institutions have now made the OFL their default recommended license for fonts.</p>
<p></p>
<a name='2c029872'></a>
<h3>OFL fonts</h3>

<p>We intend to use the OFL for all our future font releases, and will re-release our existing and older font packages under the OFL as we have personnel time. The priority of older packages will depend on demand.</p>
<p>If you release (or intend to release) your font(s) under the OFL, let us know and we may place a link to the fonts on our <a href='page.php?item_id=OFL_fonts'>OFL fonts page</a>.</p>
<a name='43a3bb12'></a>
<h2>Details and rationale</h2>

<a name='6c6c80bc'></a>
<h3>FLOSS-friendliness</h3>

<p>The OFL is designed to be in tune with the FLOSS (Free/Libre and Open Source Software) culture. It builds upon good ideas already in existence in some free/libre and open projects but by bringing our extensive font design experience and linguistic software engineering know-how into the mix, we have produced a font-specific license which is simpler, more human-readable, neutral and reusable and dedicated to the needs of font creators.</p>
<p>The OFL authors were inspired by the partnership between <a href='http://www.gnome.org/' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;GNOME</a> and <a href='http://www.bitstream.com/' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;Bitstream</a> for the <a href='http://www.gnome.org/fonts/' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;Vera family of fonts</a> and the licensing model which was chosen. They have also studied the community impact and some of the difficulties faced by this model.</p>
<a name='eceee56d'></a>
<h4>The 4 FSF Freedoms</h4>

<p>The OFL is listed and recognized as a valid Free Software license on the FSF <a href='http://www.fsf.org/licensing/licenses' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;License List</a>. It complies with the <a href='http://www.fsf.org/licensing/essays/free-sw.html' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;Free Software Definition</a> and its four foundational freedoms as defined by the Free Software Foundation for the GNU project:</p>
<ul class='dListUnordered'>
<li>Use: the freedom to use font software for any purpose. (freedom 0)</li>
<li>Study and adaptation: the freedom to study how font software works, and adapt it to your needs (freedom 1). Access and rights to the source code is a precondition for this.</li>
<li>Redistribution: the freedom to redistribute copies of the font software so you can help your neighbor (freedom 2).</li>
<li>Improvement and redistribution of modifications: the freedom to improve the font software and release your improvements (freedom 3), so that the community benefits. Access and rights to the source code is a precondition for this.</li>
</ul>
<a name='0d1d4c4d'></a>
<h4>DFSG compatibility</h4>

<p>Font Software released under the OFL complies with the <a href='http://www.debian.org/social_contract' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;Debian Free Software Guidelines</a>:</p>
<ul class='dListUnordered'>
<li>reselling: DFSG #1</li>
<li>source code redistribution: DFSG #2</li>
<li>derivatives: DFSG #3</li>
<li>"compromise" clause permitting name change: DFSG #4 (this is very important for font derivatives for artistic integrity and anti-collision purposes)</li>
<li>no discrimination against people/groups: DFSG #5</li>
<li>no discrimination against fields of endeavour: DFSG #6</li>
<li>license distribution: DFSG #7</li>
<li>non-Debian specific: DFSG #8</li>
<li>no contamination of other software: DFSG #9</li>
</ul>
<p>Various font families under OFL have been accepted in the main archive of Debian (as well as Ubuntu) by the ftp-masters. An increasing number of Debian and Ubuntu developers are maintaining font packages under the OFL in main (the component of the archive which only holds Free/Libre and Open Source software).</p>
<p></p>
<a name='8656a63b'></a>
<h4>OSD compatibility</h4>

<p>The OFL complies with the <a href='http://opensource.org/docs/definition.php' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;Open Source Definition</a>:</p>
<ul class='dListUnordered'>
<li>free redistribution: #1</li>
<li>source code: #2</li>
<li>derived works: #3</li>
<li>integrity of the author(s) source code: #4 
(with the possibility of requiring a name change)</li>
<li>no discrimination against persons or groups: #5</li>
<li>no discrimination against fields of endeavour: #6</li>
<li>distribution of license: #7</li>
<li>license must not be specific to a product: #8</li>
<li>license must not restrict other software: #9</li>
<li>license must be technology-neutral: #10</li>
</ul>
<p>The OSI (Open Source Initiative) has recognized the OFL's compliance with the OSD and placed it on their <a href='http://opensource.org/licenses/alphabetical' target='_blank'><img src='../assets/icons/offsite_link.png'>&nbsp;list of approved licenses</a>.</p>
<p></p>
