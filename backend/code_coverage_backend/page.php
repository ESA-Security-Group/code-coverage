<p>This is what each icon means:</p>
<a name='2855b85f'></a>
<h4>Attribution</h4>

<div class='mediacontainer'>
  <img src='https://scripts.sil.org/cms/scripts/../sites/nrsi/media/Attribution.png' height='32' width='32'>
  <p class='Caption' style='width: 32'>requirement</p>
</div>
<div class='Indent'><p> The icon shows a person and represents the author(s).<br />
The requirement is for proper attribution of the author(s): name(s) and notice(s) must be preserved and abuse of the name(s) and reputation of the author(s) is forbidden.<br />
See condition 2) and 4) of the OFL.</p>
</div>
<a name='77f28741'></a>
<h4>Share Alike</h4>

<div class='mediacontainer'>
  <img src='https://scripts.sil.org/cms/scripts/../sites/nrsi/media/ShareAlike.png' height='31' width='32'>
  <p class='Caption' style='width: 32'>requirement</p>
</div>
<div class='Indent'><p>The icon shows a cycle and represents the way font software can be re-used by all under equivalent terms.<br />
The requirement is for derivative works to remain under the same license to encourage fair collaboration and prevent anyone from locking away contributions.<br />
See condition 5) of the OFL</p>
</div>
<a name='9eda48a4'></a>
<h4>Embedding</h4>

<div class='mediacontainer'>
  <img src='https://scripts.sil.org/cms/scripts/../sites/nrsi/media/Embedding.png' height='32' width='32'>
  <p class='Caption' style='width: 32'>permission</p>
</div>
<div class='Indent'><p>The icon shows a letter on a piece of paper and represents a font placed inside a document.<br />
The permission is for fonts to be embedded in any kind of document. This does not affect the licensing status of the document but makes it easier for documents to be used in different environments.<br />
See the first paragraph of the Permission and Conditions section as well as section 5) of the OFL.</p>
</div>
<a name='8e6f58a5'></a>
<h4>DerivativeRenaming</h4>

<div class='mediacontainer'>
  <img src='https://scripts.sil.org/cms/scripts/../sites/nrsi/media/DerivativeRenaming.png' height='32' width='32'>
  <p class='Caption' style='width: 32'>requirement </p>
</div>
<p></p>
<div class='Indent'><p>The icon shows letters A and B close to each other representing a font (A) from which another font (B) of a different shape is derived. It refers to a derivative branched from the original font and bearing a new name.<br />
The requirement is for derivative fonts to be renamed to allow branching while retaining artistic integrity.<br />
See condition 3) of the OFL</p>
</div>
<a name='5c2c6c13'></a>
<h4>BundlingWhenSelling</h4>

<div class='mediacontainer'>
  <img src='https://scripts.sil.org/cms/scripts/../sites/nrsi/media/BundlingWhenSelling.png' height='32' width='32'>
  <p class='Caption' style='width: 32'>requirement </p>
</div>
<div class='Indent'><p>The icon shows a dollar sign between parentheses. The dollar sign represents money (although there are many other currencies in the world) and the parentheses refers to the bundling.<br />
The requirement is for fonts to be bundled with software when they are sold. Fonts cannot be sold on their own. Redistribution without selling is not restricted. <br />
See condition 1) of the OFL.</p>
</div>
